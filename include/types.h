#ifndef TYPES_H
#define TYPES_H

    // signed integers
    typedef char            i8;
    typedef short int       i16;
    typedef int             i32;
    typedef long long int   i64;
    
    // unsigned integers
    typedef unsigned char           u8;
    typedef unsigned short int      u16;
    typedef unsigned int            u32;
    typedef unsigned long long int  u64;
    typedef u64 size_t;

    // floating point numbers
    typedef float   f32;
    typedef double  f64;

#endif